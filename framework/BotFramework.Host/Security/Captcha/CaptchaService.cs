    public static CaptchaResult CreateCaptcha(string uuid, int count = 6)
    {
        var key = Mulberry32.UuidToSeed(uuid);
        var rng = new Mulberry32((int)(key & 0xFFFFFFFF));

        var allItems = CaptchaItems.Select(i => (i.emoji, i.descriptions)).ToArray();
        var items = ArrayExtensions.GetSomeRandom(allItems, count, rng.Next);

        var targetId = (int)(key % count);
        var targetItem = items[targetId];
        var textsLen = targetItem.descriptions.Length;
        var targetText = targetItem.descriptions[(int)(key % textsLen)];

        var pattern = CorruptString(targetText, rng.Next);

        var shuffledItems = ArrayExtensions.Shuffle(
            items.Select((item, id) => new CaptchaItem(item.emoji, id)).ToArray(),
            System.Random.Shared.NextDouble);

        return new CaptchaResult(pattern, targetId, shuffledItems);
    }

    private static string CorruptString(string word, Func<double> getRandom)
    {
        var chars = word.ToCharArray();
        var len = chars.Length;
        var replacementsCount = (int)Math.Round(len / 4.0);

        var targetIndices = new HashSet<int>();
        while (targetIndices.Count < replacementsCount)
        {
            var randomIndex = (int)Math.Floor(getRandom() * len);
            targetIndices.Add(randomIndex);
        }

        var result = new System.Text.StringBuilder();
        for (var i = 0; i < chars.Length; i++)
        {
            if (targetIndices.Contains(i) && TypoMap.TryGetValue(chars[i], out var typos))
            {
                var options = typos.Append("").ToArray();
                result.Append(ArrayExtensions.GetRandom(options, getRandom));
            }
            else
            {
                result.Append(chars[i]);
            }
        }

        return result.ToString();
    }
